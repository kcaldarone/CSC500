#Getting the numbers and function from the user
num1 = float(input("What would you like your first number to be?\n"))
num2 = float(input("What would you like your second number to be?\n"))

print("Adding and Subtracting the Numbers (Part 1) \n")
sum = num1 + num2
diff = num1 - num2
print(str(num1) + " added to " + str(num2) + " equals " + str(sum)+ "\n" + str(num1) + " subtracted by " + str(num2) + " equals " + str(diff) + "\n")

print("Multiplying and Dividing the Number (Part 2) \n")
mult = num1 * num2
div = num1/num2
print(str(num1) + " multiplied by " + str(num2) + " equals " + str(mult)+ "\n" + str(num1) + " divided by " + str(num2) + " equals " + str(div))